package it.polito.tdp.alien;

public class Parola {
	
	String parolaAliena;
	String traduzione;
	
	
	public Parola(String parolaAliena, String traduzione) {
		super();
		this.parolaAliena = parolaAliena;
		this.traduzione = traduzione;
	}


	public String getParolaAliena() {
		return parolaAliena;
	}


	public String getTraduzione() {
		return traduzione;
	}
	
	
	
	

}
